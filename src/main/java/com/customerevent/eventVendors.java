package com.customerevent;

public class eventVendors {
	
	private int eid;
	private int vid;
	private int aid;
	
	public eventVendors(int eid, int vid, int aid) {
		this.eid = eid;
		this.vid = vid;
		this.aid = aid;
	}

	public int getEid() {
		return eid;
	}

	

	public int getVid() {
		return vid;
	}

	

	public int getAid() {
		return aid;
	}

	
	
	

}
